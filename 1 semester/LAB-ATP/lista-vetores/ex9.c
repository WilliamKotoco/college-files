/* Redigir um programa que transforme todos os caracteres de uma dada string nome[81] em maiúsculo,
 salvando esse novo conteúdo em uma nova string nomemaiusculo[81].

Dica: Use a função toupper da biblioteca <string.h>.*/
#include <string.h>
#include <stdio.h>
int main()
{
    char nome[81], nomemaiusculo[81];
    printf("Digite o nome: ");
    scanf("%s", &nome);
    strcpy(nomemaiusculo, toupper(nome));
    puts(nomemaiusculo);
    return 0;
}